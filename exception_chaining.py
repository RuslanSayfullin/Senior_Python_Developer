try:
    # Какой-то код, который может вызвать исключение
    result = 10 / 0
except ZeroDivisionError as e:
    # Обработка первичного исключения 
    print(f"Ошибка: {e}")
    try:
        # Вложенный код, который также может вызвать исключение
        result = int("abc")
    except ValueError as e:
        # Сцепление исключений, сохранение информации о предыдущем исключении
        raise ValueError("Ошибка преобразования строки в число") from e
